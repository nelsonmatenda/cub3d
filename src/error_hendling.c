#include "cub3D.h"

void	ft_error(char *s)
{
	int	i;

	i = 0;
	write (2, "Error\n", 6);
	while (s[i] != '\0')
	{
		write(2, &s[i], 1);
		i++;
	}
	exit(1);
}
